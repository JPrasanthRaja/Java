package classandobject;

/*
  Java Classes:

  * A class in Java is a set of objects which shares common characteristics/ behavior and common properties/ attributes.
  * It is a user-defined blueprint or prototype from which objects are created. 
  * For example, Student is a class while a particular student named Ravi is an object.
  
  Properties of Java Classes:
  
 * Class is not a real-world entity. It is just a template or blueprint or prototype from which objects are created.
 * Class does not occupy memory.
 * Class is a group of variables of different data types and a group of methods.

A Class in Java can contain:

* Data member
* Method
* Constructor
* Nested Class
* Interface

Class Syntax:

access_modifier class <class_name>
{  
    data member;  
    method;  
    constructor;
    nested class;
    interface;
}

Difference between Java Class and Objects:
                Class                                                  
   * Class is the blueprint of an object. It is used to create objects. 
   * No memory is allocated when a class is declared.
   * A class is a group of similar objects.
   * Class is a logical entity.
   * A class can only be declared once.           
   * An example of class can be a car.          
  
*/

